package com.bjworld.groupware.maintenance.service.impl;

public class MaintenanceVO {
	
	private String seq;
	private String ProjectSeq;
	private String requestName;
	private String requestContent;
	private String requestDate;
	private String proResult;
	private String proDate;
	private String proState;
	private String projectName;
	
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getProjectSeq() {
		return ProjectSeq;
	}
	public void setProjectSeq(String projectSeq) {
		ProjectSeq = projectSeq;
	}
	public String getRequestName() {
		return requestName;
	}
	public void setRequestName(String requestName) {
		this.requestName = requestName;
	}
	public String getRequestContent() {
		return requestContent;
	}
	public void setRequestContent(String requestContent) {
		this.requestContent = requestContent;
	}
	public String getRequestDate() {
		return requestDate;
	}
	public void setRequestDate(String requestDate) {
		this.requestDate = requestDate;
	}
	public String getProResult() {
		return proResult;
	}
	public void setProResult(String proResult) {
		this.proResult = proResult;
	}
	public String getProDate() {
		return proDate;
	}
	public void setProDate(String proDate) {
		this.proDate = proDate;
	}
	public String getProState() {
		return proState;
	}
	public void setProState(String proState) {
		this.proState = proState;
	}
	public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
		

}
