package com.bjworld.groupware.companyhanin.service.impl;

import com.bjworld.groupware.common.vo.DefaultVO;

public class CompanyHaninVO extends DefaultVO {
    private String seq;
private String companyName;
private String companyWebsite;
private String companyAddress;
private String companyPhone;
private String companyCategory;
private String companyProduct;
private String regDate;


    public String getSeq() { return seq; }
public void setSeq(String seq) { this.seq = seq; }
public String getCompanyName() { return companyName; }
public void setCompanyName(String companyName) { this.companyName = companyName; }
public String getCompanyWebsite() { return companyWebsite; }
public void setCompanyWebsite(String companyWebsite) { this.companyWebsite = companyWebsite; }
public String getCompanyAddress() { return companyAddress; }
public void setCompanyAddress(String companyAddress) { this.companyAddress = companyAddress; }
public String getCompanyPhone() { return companyPhone; }
public void setCompanyPhone(String companyPhone) { this.companyPhone = companyPhone; }
public String getCompanyCategory() { return companyCategory; }
public void setCompanyCategory(String companyCategory) { this.companyCategory = companyCategory; }
public String getCompanyProduct() { return companyProduct; }
public void setCompanyProduct(String companyProduct) { this.companyProduct = companyProduct; }
public String getRegDate() { return regDate; }
public void setRegDate(String regDate) { this.regDate = regDate; }

}
