package com.bjworld.groupware.customerstaff.service.impl;

public class CustomerStaffVO {
	private String seq;
	private String csSeq;
	private String staffName;
	private String staffDepartment;
	private String staffRank;
	private String staffPhone;
	private String staffEmail;
	private String staffNote;
	private String csName;
	
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getCsSeq() {
		return csSeq;
	}
	public void setCsSeq(String csSeq) {
		this.csSeq = csSeq;
	}
	public String getStaffName() {
		return staffName;
	}
	public void setStaffName(String staffName) {
		this.staffName = staffName;
	}
	public String getStaffDepartment() {
		return staffDepartment;
	}
	public void setStaffDepartment(String staffDepartment) {
		this.staffDepartment = staffDepartment;
	}
	public void setStaffRank(String staffRank) {
		this.staffRank = staffRank;
	}
	public String getStaffRank() {
		return staffRank;
	}
	public String getStaffPhone() {
		return staffPhone;
	}
	public void setStaffPhone(String staffPhone) {
		this.staffPhone = staffPhone;
	}
	public String getStaffEmail() {
		return staffEmail;
	}
	public void setStaffEmail(String staffEmail) {
		this.staffEmail = staffEmail;
	}
	public String getStaffNote() {
		return staffNote;
	}
	public void setStaffNote(String staffNote) {
		this.staffNote = staffNote;
	}
	public String getCsName() {
		return csName;
	}
	public void setCsName(String csName) {
		this.csName = csName;
	}
}
